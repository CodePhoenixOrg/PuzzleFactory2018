				LeeLa est un projet, actuellement en version alpha, dont le but est de concevoir et déployer des sites web dynamiques.<p>

				LeeLa est la contraction de : 
				L'Enfant Et L'Arraignée. 

				Même si c'est moins clair, je trouve qu'un titre à la Fable de La Fontaine c'est plus original que Visual Web DevStudio. Non ?<p>

				Ce qui signifie que c'est un logiciel créatif (comme l'enfant) dédié au web (comme la toile d'arraignée) dynamique (utilisation de php et perl à l'intérieur même du programme).<p>

				PHP est utilisé via un serveur Apache local pour afficher des données qui ne nécessitent pas de traitement (Recordsets, ...), Perl est utilisé pour s'interfacer localement avec LeeLa (récupération de données à traiter par le programme).<p>

				J'utilise actuellement MySQL comme SGBDR, mais vu les possibilités de PHP et Perl-DBI, il doit être possible d'interfacer d'autres SGBDR (le très puissant PostgreSQL en perticulier).<p>

				J'aimerais aussi porter LeeLa sur d'autres plate-formes, en particulier Java. Il sera bientôt possible de le porter sous Linux avec l'arrivée de Kylix au cours des 6 mois à venir.<p>

