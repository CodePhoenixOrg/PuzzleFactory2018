				sur plusieurs systèmes ou qui le seront dans un proche avenir. Il en découle les choix suivants :<br><br>
				<ul>	<a name="delphi">DELPHI en tant qu'environnement RAD principal :<br></a>
					<ul>	Facile à appréhender comme Visual Basic et puissant comme le C++,<br>
						c'est l'EDI RAD incontestable mais contesté de la plate-forme Win32.<br>
						En effet, il est capable de faire depuis sa version 1.0 ce que Visual<br>
						Basic	ne <b><i>fera</i></b> que dans sa version <a href="http://www.mvps.org/vb/index.html?rants/dotnot.htm">Visual Fred .Not<a>. A savoir, une <b><i>vraie</i></b><br>
						approche objet, et donc la possibilité de créer des objets visuels ou<br>
						non à la volée. Vraiment, j'ai du mal à comprendre pourquoi cet outil<br>
						RAD n'a pas percé et surtout c'est encore une fois Microsoft qui rafle<br>
						la mise en <i>"révolutionnant notre façon de développer"</i> avec sa soit-disant<br>
						"technologie" .Net. Tout ça est point net. Enfin, Delphi sera représenté<br>
						par KYLIX sous Linux. Pas VB! :^)<br>
					</ul>
				</ul>
				<ul>	<a name="php">PHP pour les scripts côté serveur :<br></a>
					<ul>	Rapide et puissant car large de compétences, le langage PHP est un<br>
						choix évident pour les scripts côté serveur. De plus, contrairement<br>
						à ASP, PHP est multi-plate-forme. J'utilise les scripts PHP dans LeeLa<br>
						pour renvoyer des données qui ne nécessitent pas de traitement comme<br>
						les résultats de requêtes SELECT par exemple.
					</ul>
				</ul>
				<ul>	<a name="perl">Perl-DBI pour les scripts côté client :<br></a>
					<ul>	Le besoin d'un langage de script côté client est dû à la nécessité de<br>
						faire traiter certaines informations par le programme. Hors, PHP est<br>
						incapable de fournir des informations à une autre application cliente<br>
						que le navigateur. Mon choix s'est donc tourné vers Perl et son interface<br>
						de bases de données DBI. Les scripts Perl écrivent des résultats de<br>
						requêtes en local et LeeLa les récupère pour les afficher sans utiliser<br>
						le navigateur.
					</ul>
				</ul>
				<ul>	<a name="mysql">MySQL en tant que SGBDR principal :<br></a>
					<ul>	Si MySQL est encore jeune et incomplet (pas de procédures stockées, etc.),<br>
						il faut lui reconnaître certaines qualités, à savoir sa rapidité et sa très<br>
						grande popularité auprès des webmasters, et donc des fournisseurs d'accès.<br>
						De plus, l'accès aux bases de données MySQL est depuis peu optimisé grâce <br>
						à son intégration dans le moteur de PHP4.
					</ul>
				</ul>
				<ul>	<a name="apache">Apache en tant que serveur web :<br></a>
					<ul>	C'est incontestable, Apache est présent sur <b><i>toutes</i></b> les plate-formes.<br>
						Il est efficace, rapide et modulaire à l'instar de Perl avec lequel il dialogue<br>
						très bien d'ailleurs. Cerise sur le gateau, ce logiciel est l'un des meilleurs<br>
						exemples que le logiciel libre est viable commercialement puisqu'il écrase<br>
						littéralement son concurrent le plus vorace : IIS de Microsoft.
					</ul>
				</ul>
