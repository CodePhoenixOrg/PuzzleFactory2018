				L'intérêt de ces composants réside dans le fait qu'il n'est plus utile de concevoir un site web pour tirer partie des possibilités de PHP et finalement de visualiser le résultat dans un navigateur web. Les WinPieces permettent ainsi d'écrire des applications natives Windows/Linux indépendantes donnant un aspect plus professionnel aux applications orientées internet et intranet.<p> 
				Plusieurs séries de composants sont prévues pour couvrir à peu près tous les domaines du PHP. Mais la première série concerne la part la plus importante du PHP : l'accès aux bases de données.<p>
				Cette série de composants se nomme POP Data Objects et comprend les composants suivants : <p>
				<UL>
					<LI>TpopConnection,</LI>
					<LI>TpopDataSet,</LI>
					<LI>TpopGrid,</LI>
					<LI>TpopComboBox,</LI>
					<LI>TpopListBox.</LI>
					<P>
				</UL>
				Les composants TpopConnection sert à se connecter à une base de donnée et un site web sur lequel sont stockés des scripts PHP de traitement des données. TpopDataSet est un composant de requêtes SQL qui récupère les données du serveur SQL via les script PHP et compose un jeu d'enregistrements (RecordSet) prêt à l'emploi. Les autres composants servent à l'affichage du recordset; la liste n'est pas exhaustive, d'autres composants d'affichage doivent compléter cette liste.<p>
			</UL>
